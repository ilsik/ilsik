package model;

public class CinemaBean {
	private String cinema_title;
	private String cinema_time;
	private String cinema_type;
	private int cinema_theater;
	private int cinema_price;
	
	public CinemaBean() {}

	public String getCinema_title() {
		return cinema_title;
	}

	public void setCinema_title(String cinema_title) {
		this.cinema_title = cinema_title;
	}

	public String getCinema_time() {
		return cinema_time;
	}

	public void setCinema_time(String cinema_time) {
		this.cinema_time = cinema_time;
	}

	public String getCinema_type() {
		return cinema_type;
	}

	public void setCinema_type(String cinema_type) {
		this.cinema_type = cinema_type;
	}

	public int getCinema_theater() {
		return cinema_theater;
	}

	public void setCinema_theater(int cinema_theater) {
		this.cinema_theater = cinema_theater;
	}

	public int getCinema_price() {
		return cinema_price;
	}

	public void setCinema_price(int cinema_price) {
		this.cinema_price = cinema_price;
	}
	
	
}
