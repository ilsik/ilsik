package model;

public class MovieBean {
	private String movie_title;
	private int movie_audience;
	private double movie_grade;
	private String movie_start;
	private int movie_runningTime;
	private int movie_age;
	private String movie_genre;
	
	public MovieBean() {}

	public String getMovie_title() {
		return movie_title;
	}

	public void setMovie_title(String movie_title) {
		this.movie_title = movie_title;
	}

	public int getMovie_audience() {
		return movie_audience;
	}

	public void setMovie_audience(int movie_audience) {
		this.movie_audience = movie_audience;
	}

	public double getMovie_grade() {
		return movie_grade;
	}

	public void setMovie_grade(double movie_grade) {
		this.movie_grade = movie_grade;
	}

	public String getMovie_start() {
		return movie_start;
	}

	public void setMovie_start(String movie_start) {
		this.movie_start = movie_start;
	}

	public int getMovie_runningTime() {
		return movie_runningTime;
	}

	public void setMovie_runningTime(int movie_runningTime) {
		this.movie_runningTime = movie_runningTime;
	}

	public int getMovie_age() {
		return movie_age;
	}

	public void setMovie_age(int movie_age) {
		this.movie_age = movie_age;
	}

	public String getMovie_genre() {
		return movie_genre;
	}

	public void setMovie_genre(String movie_genre) {
		this.movie_genre = movie_genre;
	}
	
}
